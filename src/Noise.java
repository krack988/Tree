
public class Noise {
    int crona,wind,poraGoda;
    Noise(int crona, int wind, int poraGoda){
        this.crona=crona;
        this.wind=wind;
        this.poraGoda=poraGoda;
    }
    public int getCrona(){
        return crona;
    }
    public int getWind(){
        return wind;
    }
    public int getPoraGoda(){
        return poraGoda;
    }
}
